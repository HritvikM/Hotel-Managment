    private void CADFrameWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_CADFrameWindowOpened
        // TODO add your handling code here:
        jTextField2.setText(useridmain + "");
        try{
            Connection conn=null;
            Statement stmt=null;
            ResultSet rs=null, rs1=null;
            String url="jdbc:mysql://localhost/Indana_Palace", userid="root";
            conn=DriverManager.getConnection(url, userid, pwd);
            stmt=conn.createStatement();
            String query ="Select * from Id where UserId='"+useridmain+"';";
            String idtype, query1;
            if(idtypemain.equals("Staff"))
                idtype="staff";
            else
                idtype="cust";
            query1="Select * from "+idtype+" where UserId='"+useridmain+"';";
            rs=stmt.executeQuery(query);
            rs.first();
            String fname, lname, gen, email, mobile, nation, address, seqques, seqans;
            fname=rs.getString("First_Name");
            lname=rs.getString("Last_Name");
            seqques=rs.getString("Security_ques");
            seqans=rs.getString("Answer");
            rs=stmt.executeQuery(query1);
            rs.first();
            gen=rs.getString("Gender");
            email=rs.getString("Email");
            mobile=rs.getString("Mobile_No");
            nation=rs.getString("Nationality");
            address=rs.getString("Address");
            FirstNmTF2.setText(fname);
            LastNmTF2.setText(lname);
            if(gen.equals("Male"))
                jRadioButton6.setSelected(true);
            else
                jRadioButton7.setSelected(true);
            EmailIDTF2.setText(email);
            MobileNoTF2.setText(mobile);
            jComboBox3.setSelectedItem(nation);
            jTextArea2.setText(address);
            SecQuesCombo2.setSelectedItem(seqques);
            SecQuesAnsTF2.setText(seqans);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_CADFrameWindowOpened

