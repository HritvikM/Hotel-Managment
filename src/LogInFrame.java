    private void PasswordTF1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_PasswordTF1FocusLost
        // TODO add your handling code here:
        String pass= new String(PasswordTF1.getPassword());
        int i,alpha=0, special=0, num=0;
        char c;
        for(i=0;i<pass.length();i++){
            c=pass.charAt(i);
            if(c>=97 && c<=122)
                alpha++;
            else if(c>=48 && c<=57)
                num++;
            else
                special++;
        }
        if(alpha!=0 && num!=0 && special!=0)
            jLabel195.setText("");
        else if(pass.length()<8)
            jLabel195.setText("<html>Password must be atleast 8 characters long</html>");
        else
            jLabel195.setText("<html>Password must contain atleast 1 of alphabets, numbers and special characters each</html>");
    }//GEN-LAST:event_PasswordTF1FocusLost

