        // TODO add your handling code here:
        useridmain=UserIDTF.getText();
        try{
            Connection conn=null;
            Statement stmt=null;
            ResultSet rs=null, rs1=null;
            String url="jdbc:mysql://localhost/Indana_Palace", userid="root";
            conn=DriverManager.getConnection(url, userid, pwd);
            stmt=conn.createStatement();
            String query ="Select * from Id where UserId='"+useridmain+"';";
            rs =stmt.executeQuery(query);
            rs.first();
            String acpass=rs.getString("Password");
            String enpass=new String(PasswordTF.getPassword());
            String enIdtype, acIdtype;
            int enadminstatus, acadminstatus;
            if(StaffRB.isSelected())
                enIdtype="Staff";
            else
                enIdtype="Customer";
            if(AdminCB.isEnabled() && AdminCB.isSelected())
                enadminstatus=1;
            else
                enadminstatus=0;
            acIdtype=rs.getString("ID_Type");
            acadminstatus=rs.getInt("If_Admin");
            if(enIdtype.equals(acIdtype))
                idtypemain=acIdtype;
            else{
                JOptionPane.showMessageDialog(null, "Please check your ID Type");
                idtypemain="";
            }
            if(enadminstatus==acadminstatus)
                adminstatusmain=acadminstatus;
            else{
                JOptionPane.showMessageDialog(null, "Please check if your ID has admin previliges or uncheck the admin check box");
                 adminstatusmain=10;
            }
            if(!enpass.equals(acpass))
                JOptionPane.showMessageDialog(null, "Incorrect Password");
            if(idtypemain.equals("Staff") && enpass.equals(acpass) && enadminstatus==adminstatusmain){
                StaffMainFrame.setVisible(true);
                this.setVisible(false);
            }
            else if(idtypemain.equals("Customer") && enpass.equals(acpass) && enadminstatus==adminstatusmain){
                CustMainFrame.setVisible(true);
                this.setVisible(false);
            }
            else
                JOptionPane.showMessageDialog(null, "Please check your login details again");
            if(enpass.equals(acpass))
                passwordmain=acpass;
            String query1=null;
            if(idtypemain.equals("Staff")){
                query1="Select * from staff";
                rs1=stmt.executeQuery(query1);
                rs1.first();
                maindeptstaff=rs1.getString("Dept");
            }
            rs.close();
            stmt.close();
            conn.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
