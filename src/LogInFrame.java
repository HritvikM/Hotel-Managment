                String date=df.format(jDateChooser3.getDate());
                String query="Insert into booked values('"+user+"','card',"+tablfor+",'"+from1+"','"+to1+"','"+date+"');";
                p=stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Payment Successfull");
